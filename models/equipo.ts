class Equipo{
    public frigoriasEnfriador:number;
    public volumenEnfriador:string;
    public precioEnfriador:number;

    constructor(frigoriasEnfriador:number, volumenEnfriador:string, precioEnfriador:number){
        this.frigoriasEnfriador=frigoriasEnfriador;
        this.volumenEnfriador=volumenEnfriador;
        this.precioEnfriador=precioEnfriador;
    }

}